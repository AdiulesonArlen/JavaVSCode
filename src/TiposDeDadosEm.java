public class TiposDeDadosEm {
    public static void main(String[] args) throws Exception {
        //Variáveis do tipo Iinteiro:
        byte numeroPequeno = 125;
        short numeroShort = 32767;
        int numeroMaior = 2147483647;
        long numeroEnorme = 922337203685477000l;
        
        //Variáveis do tipo ponto Flutuante:
        float peso = 78.7f;
        double pi = 3.1484941648431184131518;

        //Variáveis do tipo Caractere:
        char letraOuSimbolo = '+';

        //Variáveis do tipo Booleano:
        boolean estouFaminto = false;

        //Variáveis do tipo String:
        String texto = "Olá Mundo!";

        System.out.println(texto);
        

    }
}
